public class Traffic {
    private String district_hash;
    private String tj_level;
    private String tj_time;
    private String trafficTime;

    public String getDistrict_hash() {
        return district_hash;
    }

    public void setDistrict_hash(String district_hash) {
        this.district_hash = district_hash;
    }

    public String getTj_level() {
        return tj_level;
    }

    public void setTj_level(String tj_level) {
        this.tj_level = tj_level;
    }

    public String getTj_time() {
        return tj_time;
    }

    public void setTj_time(String tj_time) {
        this.tj_time = tj_time;
    }

    public String getTrafficTime() {
        return trafficTime;
    }

    public void setTrafficTime(String trafficTime) {
        this.trafficTime = trafficTime;
    }
}
